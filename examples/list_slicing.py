# Clarifies behaviour of list slicing and indexing.
# Keywords:
# list / slicing / slice / fragmentation / index / indexing

my_list = [1, 2, 3, 4, 5, 6, 7]

print(my_list[0])  # 1
print(my_list[-1])  # 7

print(my_list[1:])  # [2, 3, 4, 5, 6, 7]
print(my_list[:3])  # [1, 2, 3]
print(my_list[3:5])  # [4, 5]
